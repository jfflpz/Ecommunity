    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String id=jTextField1.getText();
        String oldPass=new String(jPasswordField1.getPassword());
        String newPass=new String(jPasswordField2.getPassword());
        String cNewPass=new String(jPasswordField3.getPassword());

        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/test","root","jefflopez");
            PreparedStatement st=con.prepareStatement("select * from addadmin where id=? and pass=?");
            st.setString(1,id);
            st.setString(2,oldPass);
            ResultSet  rs=st.executeQuery();
            if(rs.next())
            {
                if(newPass.equals(cNewPass))
                {
                    PreparedStatement st1=con.prepareStatement("update addadmin set pass=? where id=?");
                    st1.setString(1,newPass);
                    st1.setString(2,id);
                    st1.executeUpdate();
                    JOptionPane.showMessageDialog(this,"Password Updated");
                    new AdminLogin().setVisible(true);
                    dispose();
                }
                else
                {
                    JOptionPane.showMessageDialog(this,"Your New Passwords Don't Match");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Invalid Id/Old Password");
            }
        }catch(SQLException | ClassNotFoundException ex)
        {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

